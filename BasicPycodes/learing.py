# FizzBuzz: Print numbers 1-100, replace multiples of 3 with "Fizz", 5 with "Buzz", both with "FizzBuzz".

for i in range(1,101):
    if i%3==0 and i%5==0:
        print("FizzBuzz", i)
    elif i%3==0:
        print("Fizz",i)
    elif i%5==0:
        print("Buzz",i)
